#mostre o seno, cosseno e tangente de um 창ngulo
import math

ang = float(input("Digite o valor do 창ngulo desejado: "))

print(f"Para o 창ngulo {ang}째, temos {math.cos(math.radians(ang)):.2f} como cosseno, {math.sin(math.radians(ang)):.2f}"
      f" como seno e {math.tan(math.radians(ang)):.2f} como tangente.")